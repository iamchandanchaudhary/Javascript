console.log("Type Conversion :- \n");

// Int(number) type
let a = 43;
console.log("Data Type of A : " + typeof a);

// Boolean type
a = true;
console.log("Data Type of A : " + typeof a);

// String type
a = "Chandan Chaudhary";
console.log("Data Type of A : " + typeof a);

// float(number) type
a = 23.5;
console.log("Data Type of A : " + typeof a);

// char(string) type
a = '@';
console.log("Data Type of A : " + typeof a);

// (string) type
a = ' ';
console.log("Data Type of A : " + typeof a);